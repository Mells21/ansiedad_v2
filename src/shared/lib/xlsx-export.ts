const textEncoder = new TextEncoder();

type XlsxCellValue = string | number;

export interface XlsxCell {
  style?: number;
  value: XlsxCellValue;
}

type XlsxRow = Array<XlsxCellValue | XlsxCell>;

interface XlsxSheetOptions {
  columnWidths?: number[];
  fileName: string;
  merges?: string[];
  rowHeights?: number[];
  rows: XlsxRow[];
  sheetName: string;
}

interface ZipEntry {
  bytes: Uint8Array;
  crc32: number;
  name: string;
}

function escapeXml(value: string) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}

function columnName(index: number) {
  let value = index + 1;
  let result = "";

  while (value > 0) {
    const remainder = (value - 1) % 26;
    result = String.fromCharCode(65 + remainder) + result;
    value = Math.floor((value - 1) / 26);
  }

  return result;
}

function normalizeCell(cell: XlsxCellValue | XlsxCell): XlsxCell {
  if (typeof cell === "object" && cell !== null && "value" in cell) {
    return cell;
  }

  return { value: cell };
}

function toCellXml(cell: XlsxCellValue | XlsxCell, rowIndex: number, columnIndex: number) {
  const normalized = normalizeCell(cell);
  const reference = `${columnName(columnIndex)}${rowIndex + 1}`;
  const styleAttr = normalized.style !== undefined ? ` s="${normalized.style}"` : "";

  if (typeof normalized.value === "number") {
    return `<c r="${reference}"${styleAttr}><v>${normalized.value}</v></c>`;
  }

  return `<c r="${reference}" t="inlineStr"${styleAttr}><is><t>${escapeXml(normalized.value)}</t></is></c>`;
}

function buildColumnsXml(columnWidths?: number[]) {
  if (!columnWidths || columnWidths.length === 0) {
    return "";
  }

  const columns = columnWidths
    .map(
      (width, index) =>
        `<col min="${index + 1}" max="${index + 1}" width="${width}" customWidth="1"/>`,
    )
    .join("");

  return `<cols>${columns}</cols>`;
}

function buildMergeCellsXml(merges?: string[]) {
  if (!merges || merges.length === 0) {
    return "";
  }

  const refs = merges.map((merge) => `<mergeCell ref="${merge}"/>`).join("");
  return `<mergeCells count="${merges.length}">${refs}</mergeCells>`;
}

function buildSheetXml(rows: XlsxRow[], columnWidths?: number[], merges?: string[], rowHeights?: number[]) {
  const sheetRows = rows
    .map((row, rowIndex) => {
      const cells = row.map((value, columnIndex) => toCellXml(value, rowIndex, columnIndex)).join("");
      const height = rowHeights?.[rowIndex];
      const rowAttr = height ? ` ht="${height}" customHeight="1"` : "";
      return `<row r="${rowIndex + 1}"${rowAttr}>${cells}</row>`;
    })
    .join("");

  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<worksheet xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main">
  <sheetViews>
    <sheetView workbookViewId="0"/>
  </sheetViews>
  <sheetFormatPr defaultRowHeight="18"/>
  ${buildColumnsXml(columnWidths)}
  <sheetData>${sheetRows}</sheetData>
  ${buildMergeCellsXml(merges)}
</worksheet>`;
}

